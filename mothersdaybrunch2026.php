    <!-- ── EMAIL MARKETING ────────────────────────────────────── -->
    <h2>Email Marketing: Three Sends, Three Jobs</h2>

    <p>
      Three email blasts each had a distinct role in moving guests toward a booking.
      The first led with the menu — snow crab legs, fresh-shucked oysters, Salmon Wellington — to anchor the value proposition.
      The second created urgency around the early bird discount expiring in two days.
      The third introduced the Flower Bar add-on and the Penthouse Suite photo contest, giving people who had already seen
      the first two emails a new reason to click.
    </p>

    <a href="#"><img src="images/blog/mothersdaybrunch2026/email-1.jpg" alt="First email blast: menu-focused with seafood hero" /></a>
