    <!-- ── THE CHALLENGE ──────────────────────────────────────── -->
    <h2>The Challenge: Starting With Nothing</h2>

    <p>
      Most hospitality campaigns inherit something — last year's photos, a library of food shots, an existing email list that already knows the venue.
      For this campaign, we had none of that. MIXT's Mother's Day history lived in the memories of regulars and in a floor plan with 197 ticks on it.
      That meant before a single caption was written or an ad was launched, we had to build the creative foundation: branding direction,
      a photo and video shoot, décor, and a reservation system that could actually handle the volume we were planning to drive.
    </p>

    <!-- ── PROJECT SCOPE ──────────────────────────────────────── -->
    <h2>Project Scope</h2>

    <p>What a 40-day campaign actually looks like end to end:</p>

    <ul class="scope-grid">
      <li>Brand direction & colour palette</li>
      <li>Restaurant décor & feature wall</li>
      <li>Original photo & video shoot</li>
      <li>11 Instagram Reels with tailored hooks</li>
      <li>8 Meta paid ad creatives</li>
      <li>3 email marketing blasts</li>
      <li>MIXT website landing page</li>
      <li>Social media link-in-bio landing page</li>
      <li>OpenTable setup (Experience + Stripe + Squirrel)</li>
      <li>Flower Bar concept, procurement & setup</li>
      <li>Penthouse Suite photo contest</li>
      <li>Ongoing ad monitoring & optimisation</li>
    </ul>

    <!-- ── CREATIVE CONCEPT ───────────────────────────────────── -->
    <h2>The Creative Direction: Classic Spring Garden</h2>

    <p>
      The campaign needed to feel special without feeling out of reach. We landed on a Classic Spring Garden palette —
      blush pink, sage green, buttercream, and gold — that could translate across print menus, digital ads, email headers,
      and physical décor without losing coherence. The Barnston banquet hall was decorated to match, creating an environment
      guests actually wanted to photograph and share.
    </p>

