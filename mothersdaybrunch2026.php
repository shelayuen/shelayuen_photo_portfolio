    <!-- ── THE FLOWER BAR ─────────────────────────────────────── -->
    <h2>The Flower Bar: The Detail That Brought People Back</h2>

    <p>
      What started as a feature wall idea expanded into a full Flower Bar — a complimentary station where guests could
      build their own bouquet and gift it to Mum at the table. It wasn't in the original brief.
      It required sourcing flowers, coordinating pickup on May 6, and assembling the full setup by May 8.
      And it became the single most-talked-about element of the entire event.
    </p>

    <p>
      Guests who hadn't visited in two years came specifically because they'd seen the Flower Bar on social media.
      They stopped to take photos with the arrangements, shared them on Instagram, and before leaving asked whether
      we'd be doing something similar for Father's Day. Frontline servers noticed the difference too —
      a beautifully dressed room made guests feel genuinely celebrated, and that feeling showed up in how
      they tipped and how they talked about the experience on the way out.
    </p>

    <a href="#"><img src="images/blog/mothersdaybrunch2026/flower-bar.jpg" alt="Complimentary Flower Bar at MIXT Mother's Day Brunch 2026" /></a>

    <!-- ── THE RESULTS ─────────────────────────────────────────── -->
    <h2>The Results</h2>

    <p>
      The campaign finished with 230 covers — 17% above last year's 197 — and $14,591.93 in revenue.
      But the number that doesn't appear in any dashboard is the one that arguably matters most for a venue like MIXT:
      lapsed guests came back, had a good time, and are already planning their next visit.
      That kind of re-engagement is what a single well-executed campaign can do when it gives people a genuine reason to show up.
    </p>

    <!-- ── THE TAKEAWAY ────────────────────────────────────────── -->
    <h2>The Takeaway</h2>

    <p>
      This campaign was built from nothing — no archive of food shots, no event photography from previous years,
      no existing digital infrastructure for reservations. Every asset was created for this campaign,
      which meant every decision had to earn its place. The Flower Bar wasn't in the original plan.
      The regulars-focused ad wasn't the obvious safe choice. But both were the right calls,
      because they were rooted in something real: the specific guests MIXT actually wants back in the room.
      The data confirmed what the room already knew — people don't come back for a buffet,
      they come back because somewhere made them feel like the most important person there.
    </p>

