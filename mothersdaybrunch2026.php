    <!-- ── THE MENU & ASSETS ───────────────────────────────────── -->
    <h2>Building the Assets: Menu, Food Photography & Reels</h2>

    <p>
      With no prior food photography to pull from, we organised a dedicated shoot to capture the spread — 50+ dishes across
      boards, a chef-attended carving station, breakfast and brunch items, a kids' station, and desserts.
      The images became the backbone of every channel: paid ads, email headers, landing pages, and organic Reels.
    </p>

    <a href="#"><img src="images/blog/mothersdaybrunch2026/food-spread.jpg" alt="Mother's Day Brunch buffet spread at MIXT Lobby Lounge" /></a>
    <a href="#"><img src="images/blog/mothersdaybrunch2026/food-closeup.jpg" alt="Close-up of plated entrée at MIXT Mother's Day Brunch" /></a>

    <p>
      Eleven Reels were scripted around distinct creative pillars — Grand Scale, Luxury, Urgency, Value, Guilt, Aesthetic, FOMO,
      and more — each opening with a hook engineered to stop the scroll. The goal was to hit different audience mindsets
      rather than repeating the same message in different wrapping.
    </p>

