    <!-- ── LANDING PAGE ────────────────────────────────────────── -->
    <h2>The Funnel: Two Landing Pages, One Job</h2>

    <p>
      Every ad and email pointed to a dedicated MIXT landing page built to remove friction between interest and booking.
      The full menu was visible without a click, the OpenTable widget was embedded directly on the page,
      and an early-bird savings banner reinforced urgency at the top. A separate social media link-in-bio page
      housed both the Mother's Day offer and the Meat Lovers April promotion side by side,
      keeping the Sheraton's Instagram bio clean and functional throughout the campaign.
    </p>

    <a href="#"><img src="images/blog/mothersdaybrunch2026/landing-page.jpg" alt="MIXT Mother's Day Brunch landing page with OpenTable embed" /></a>
