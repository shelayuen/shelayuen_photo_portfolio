								<p>Valentine's Day is one of the most competitive dining and hospitality moments of the year. For 2026, my client — a boutique hotel with an in-house restaurant — wanted to do more than just run a promotion. They wanted to build desire, drive qualified traffic, and convert that attention into real bookings across two distinct offerings: a Romance Hotel Room Package and a Valentine's Special Menu at the hotel's restaurant.</p>
								
								<blockquote class="wp-block-quote">
									<p> "The goal wasn't just exposure — it was a seamless journey from the first Instagram reel to a confirmed reservation."</p>
									<cite></cite>
								</blockquote>

								<h2>Campaign Timeline</h2>
								<div class="timeline">
									<div class="timeline-item">
										<strong>Week 1 — Jan 26–Feb 1</strong>
										<p>Photo &amp; video production, website launch, content calendar activation, email blast #1, Google Ads live, Meta Ads campaign launch, OpenTable &amp; Google Business Profile updates.</p>
									</div>
									<div class="timeline-item">
										<strong>Week 2 — Feb 2–8</strong>
										<p>Ongoing reel publishing (12 total), giveaway campaign kick-off in partnership with the on-site Spa, email blast #2 to restaurant subscribers, campaign performance monitoring and optimization.</p>
									</div>
									<div class="timeline-item">
										<strong>Week 3 — Feb 9–14</strong>
										<p>Final push content, countdown reels, last-minute booking conversions, ad budget shifted to highest-performing creatives, Valentine's Day execution.</p>
									</div>
								</div>


								<div class="spacer-50"></div>

								<h2>The Results</h2>
								
								<p>In two weeks, we got reservation emails flying in every time we posted something on social media. At the end we had received 300% more reservations than the week before the social campaigns, and five times our normal revenue for that month.
								</p>
								<h2>The Takeaway</h2>
								<p>Social media gets people interested, but interest doesn't equal money until you make it stupid-easy to book. Every extra click or confusing step loses you customers. When we removed all the friction between "that looks nice" and "table for four, please," the bookings rolled in.
								</p>
